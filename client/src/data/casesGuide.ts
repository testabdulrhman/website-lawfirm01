// Helper: get all unique subcategories for a category
export function getSubcategories(categoryId: string): string[] {
  const category = casesGuide.find(c => c.id === categoryId);
  if (!category) return [];
  return Array.from(new Set(category.cases.map(c => c.subcategory)));
}

// Helper: get total cases count
export function getTotalCasesCount(): number {
  return casesGuide.reduce((total, cat) => total + cat.cases.length, 0);
}
