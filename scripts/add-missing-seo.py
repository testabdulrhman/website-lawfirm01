"""إضافة الصفحات الناقصة إلى seo-data.json لإصلاح الفهرسة الأساسية."""
import json, os

BASE = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
PATH = os.path.join(BASE, "scripts", "seo-data.json")
SITE = "https://redwan.sa"
FIRM = "شركة عبدالرحمن رضوان المشيقح للمحاماة وإدارة إجراءات الإفلاس"

with open(PATH, encoding="utf-8") as f:
    data = json.load(f)

def entry(route, title, desc, keywords, faq=None, og_title=None, og_desc=None):
    e = {
        "title": f"{title} | {FIRM}",
        "description": desc,
        "keywords": keywords,
        "ogTitle": og_title or title,
        "ogDesc": og_desc or desc,
        "ogUrl": f"{SITE}{route}",
        "ogType": "website",
        "ogImage": None,
        "canonical": f"{SITE}{route}",
    }
    if faq:
        e["faq"] = faq
    return e

